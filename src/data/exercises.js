/** Gibt alle Übungen zurück (inkl. benutzerdefinierter) */
export const getAllExercises = (customExercises = []) => [
  ...exercises,
  ...customExercises,
]

/** Filtert Übungen nach Kategorie und deaktivierten IDs */
export const getFilteredExercises = (category, disabledIds = [], customExercises = []) => {
  const all = getAllExercises(customExercises)
  return all.filter(
    (e) =>
      !disabledIds.includes(e.id) &&
      (category === 'alle' || e.category === category)
  )
}

/** Wählt eine zufällige Übung (optionale Ausschluss-ID) */
export const pickRandomExercise = (category, disabledIds = [], customExercises = [], excludeId = null) => {
  let pool = getFilteredExercises(category, disabledIds, customExercises)
  if (excludeId) pool = pool.filter((e) => e.id !== excludeId)
  if (pool.length === 0) pool = getFilteredExercises(category, disabledIds, customExercises)
  if (pool.length === 0) return null
  return pool[Math.floor(Math.random() * pool.length)]
}
